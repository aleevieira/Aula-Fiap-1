
public class Incremento {
	public static void main(String[] args) {
		
		int i = 5;
		
		// primeiro eu atribuo o valor de i para a variavel x e depois somo um 
		// ao valor de i
		int x = i++; 
		
		System.out.println(x);
		
		int j = 10;
		// o + esta antes do j então eu ja somo um ao valor de j
		// é somado um ao valor de j para depois ser realizado a passagem
		// de valor para a variável
		int z = ++j;
		
		System.out.println(z);
		
		int k  = 100;
		
		int l = k--;
		
		System.out.println(k);
		
		int m = 100;
		
		int n = --m;
				
		System.out.println(m);
		
		
		
	}

}
